    fun preventLag() {
        val scheduler = Executors.newScheduledThreadPool(1)
        scheduler.scheduleAtFixedRate({
            try {
                System.gc()
            } catch (e: Exception) {
                println("${ansi().fgBrightRed()}[Error]${ansi().reset()} Failed to run garbage collection: ${e.message}")
            }
        }, 0, 1, TimeUnit.SECONDS)
    }

